class User {

    name;
    phoneNumber;
    patientName
    email;
    password;
    role;

    constructor(name, phoneNumber, patientName, email, password, role) 
    {
        this.name = name;
        this.phoneNumber = phoneNumber;
        this.patientName = patientName;
        this.email = email;
        this.password = password;
        this.role = role;
    }
}